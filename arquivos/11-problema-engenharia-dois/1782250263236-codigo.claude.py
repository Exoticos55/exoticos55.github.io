APOIO_LABELS = {0: "Livre", 1: "Móvel", 2: "Fixo"}
APOIO_CORES  = {0: "gray",  1: "orange", 2: "green"}

def _desenhar_apoio(ax, xi, yi, tipo, escala=0.15):
    """Desenha símbolo de apoio abaixo do nó."""
    s = escala * max(1, (max([xi]) - min([xi]) + 1))   # escala relativa
    if tipo == 2:   # fixo: triângulo preenchido + hachura
        tri = plt.Polygon([[xi, yi], [xi - s, yi - s], [xi + s, yi - s]],
                           closed=True, color="green", zorder=3, alpha=0.7)
        ax.add_patch(tri)
        ax.plot([xi - s, xi + s], [yi - s, yi - s], color="green", lw=2, zorder=4)
    elif tipo == 1: # móvel: triângulo + base com rodinhas
        tri = plt.Polygon([[xi, yi], [xi - s, yi - s], [xi + s, yi - s]],
                           closed=True, color="orange", zorder=3, alpha=0.7)
        ax.add_patch(tri)
        ax.plot([xi - s*1.1, xi + s*1.1], [yi - s - 0.05*s, yi - s - 0.05*s],
                color="orange", lw=1.5, zorder=4)

def plotar_trelica_original(N, B, x, y, barras, apoios, titulo="Treliça – geometria original"):
    fig, ax = plt.subplots(figsize=(10, 6))
    fig.patch.set_facecolor("#f8f9fa")
