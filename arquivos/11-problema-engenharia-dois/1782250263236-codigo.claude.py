# 3. MONTAGEM DA MATRIZ DE EQUILÍBRIO
# ════════════════════════════════════════════════════════════════════════════

def montar_sistema(N, B, x, y, barras, Fx, Fy, apoios):
    """
    Monta a matriz de equilíbrio (2N × 2N+reações) e o vetor de cargas.
    Incógnitas: forças nas B barras + reações (Rx, Ry por fixo; Ry por móvel).
    Retorna (A, b, lista_incognitas) onde lista_incognitas descreve cada coluna.
    """
    # Identifica reações
    reacoes = []   # (nó, 'x' ou 'y')
    for i, ap in enumerate(apoios):
        if ap == 2:          # fixo: Rx e Ry
            reacoes.append((i, 'x'))
            reacoes.append((i, 'y'))
        elif ap == 1:        # móvel: só Ry
            reacoes.append((i, 'y'))

    n_inc = B + len(reacoes)   # total de incógnitas
    n_eq  = 2 * N              # equações de equilíbrio

    if n_inc != n_eq:
        raise ValueError(
            f"Sistema isostático requer {n_eq} incógnitas, mas há {n_inc}. "
            "Ajuste barras/apoios."
        )

    A = [[0.0] * n_inc for _ in range(n_eq)]
    b = [0.0] * n_eq

    # Contribuição de cada barra
    for k, (ni, nj) in enumerate(barras):
        dx = x[nj] - x[ni]
        dy = y[nj] - y[ni]
        L  = math.hypot(dx, dy)
        if L < 1e-12:
            raise ValueError(f"Barra {k} tem comprimento zero (nós {ni} e {nj} coincidem).")
        cx, cy = dx / L, dy / L

        # Equação X do nó i
        A[2*ni][k] += cx
        # Equação Y do nó i
        A[2*ni+1][k] += cy
        # Equação X do nó j (sentido oposto)
        A[2*nj][k] -= cx
        # Equação Y do nó j
        A[2*nj+1][k] -= cy

    # Contribuição das reações
    for r_idx, (ni, direcao) in enumerate(reacoes):
        col = B + r_idx
        eq  = 2*ni + (0 if direcao == 'x' else 1)
        A[eq][col] = 1.0

    # Vetor b (cargas externas, sinal negativo pois passam pro outro lado)
    for i in range(N):
        b[2*i]   = -Fx[i]
        b[2*i+1] = -Fy[i]

    incognitas = [f"F_barra{k}" for k in range(B)] + \
                 [f"R_{ni}{'x' if d=='x' else 'y'}" for ni, d in reacoes]

    return A, b, incognitas


