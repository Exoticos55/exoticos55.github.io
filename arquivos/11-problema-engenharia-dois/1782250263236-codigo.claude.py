# 7. GERADOR ALEATÓRIO DE TRELIÇAS (para testar N=4,8,16,32)
# ════════════════════════════════════════════════════════════════════════════

def gerar_trelica_aleatoria(N):
    """
    Gera uma treliça plana simples em grade retangular para N nós.
    Garante isostasia básica: B = 2N - 3 barras + apoios adequados.
    """
    # Distribui N nós em grade
    cols = max(2, math.ceil(math.sqrt(N)))
    rows = math.ceil(N / cols)
    x, y = [], []
    for r in range(rows):
        for c in range(cols):
            if len(x) < N:
                x.append(float(c))
                y.append(float(r))

    # Barras horizontais e verticais da grade
    barras = set()
    for i in range(N):
        ri, ci = divmod(i, cols)
        # horizontal
        if ci + 1 < cols:
            j = i + 1
            if j < N: barras.add((min(i,j), max(i,j)))
        # vertical
        if ri + 1 < rows:
            j = i + cols
            if j < N: barras.add((min(i,j), max(i,j)))
        # diagonal
        if ci + 1 < cols and ri + 1 < rows:
            j = i + cols + 1
            if j < N: barras.add((min(i,j), max(i,j)))

    barras = sorted(barras)
    B = len(barras)

    Fx = [0.0] * N
    Fy = [0.0] * N
    # Carga no nó do meio superior
    mid = N // 2
    Fy[mid] = -1000.0

    # Apoio fixo no nó 0, móvel no último nó da base
    apoios = [0] * N
    apoios[0] = 2
    # Encontra nó mais à direita na base (y=0)
    base_nos = [i for i in range(N) if abs(y[i]) < 1e-9]
    if len(base_nos) > 1:
        apoios[base_nos[-1]] = 1

    return N, B, x, y, barras, Fx, Fy, apoios


