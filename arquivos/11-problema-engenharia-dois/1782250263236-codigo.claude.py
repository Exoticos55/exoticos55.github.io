# 1. ELIMINAÇÃO DE GAUSS (implementação manual, sem np.linalg)
# ════════════════════════════════════════════════════════════════════════════

def gauss_jordan(A_orig, b_orig):
    """
    Resolve Ax = b pelo método de Gauss-Jordan com pivotamento parcial.
    Retorna o vetor solução x ou lança ValueError se singular.
    """
    n = len(b_orig)
    # Monta matriz aumentada [A | b]
    M = [[float(A_orig[i][j]) for j in range(n)] + [float(b_orig[i])] for i in range(n)]

    for col in range(n):
        # Pivotamento parcial
        max_row = max(range(col, n), key=lambda r: abs(M[r][col]))
        M[col], M[max_row] = M[max_row], M[col]

        pivo = M[col][col]
        if abs(pivo) < 1e-12:
            raise ValueError(f"Sistema singular na coluna {col} – verifique os apoios.")

        # Normaliza linha do pivô
        M[col] = [v / pivo for v in M[col]]

        # Elimina nas demais linhas
        for row in range(n):
            if row != col:
                fator = M[row][col]
                M[row] = [M[row][j] - fator * M[col][j] for j in range(n + 1)]

    return [M[i][n] for i in range(n)]


