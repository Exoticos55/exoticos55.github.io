def classificar(forca, tol=1e-6):
    if forca > tol:
        return "Tração (+)"
    elif forca < -tol:
        return "Compressão (−)"
    else:
        return "Nula"

def estatisticas(forcas_barras, nomes_barras):
    mags = [abs(f) for f in forcas_barras]
    idx_max = mags.index(max(mags))
    media   = sum(mags) / len(mags) if mags else 0
    print("\n" + "═"*60)
    print("  ESTATÍSTICAS")
    print("═"*60)
    print(f"  Barra mais solicitada : {nomes_barras[idx_max]}  |  {forcas_barras[idx_max]:.2f} N")
    print(f"  Força média (magnitude): {media:.2f} N")
    print(f"  Força máxima           : {max(mags):.2f} N")
    print(f"  Força mínima (não nula): {min(f for f in mags if f > 1e-6):.4f} N" if any(f > 1e-6 for f in mags) else "")
    print("═"*60)


